// Reverse mapping: currency to country (for exchange rates)
export function getCountriesByCurrency(currencyCode: string): string[] {
  return Object.entries(countryCurrencyMap)
    .filter(([_, currency]) => currency === currencyCode)
    .map(([country]) => country);
}

